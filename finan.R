library(readxl)
library(writexl)
library(seasonal)

finan01 = read_xlsx("17- Sistema Financiero.xlsx",sheet = "Préstamos RC deflact MIX",col_names = FALSE,range = "B6:B120")
finan01 = ts(finan01$...1, frequency = 4, start = c(1996,1))
